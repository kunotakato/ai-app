def build_resume_prompt(
    current_job,
    experience_years,
    target_job,
    work_details,
    achievements,
    strengths,
    tone,
    career_change_mode=False,
    current_industry="",
    reason_for_change="",
    transferable_experience="",
    learning_skills="",
    portfolio="",
    job_posting="",
):
    """
    職務経歴書を生成するためのプロンプトを作成する関数です。
    未経験転職・求人票貼り付け・ポートフォリオ入力に対応しています。
    """

    career_change_instruction = ""

    if career_change_mode:
        career_change_instruction = f"""
今回は未経験職種への転職を想定しています。

現在の業界：
{current_industry}

転職したい理由：
{reason_for_change}

転職先で活かしたい経験：
{transferable_experience}

学習中のスキル：
{learning_skills}

制作物・ポートフォリオ：
{portfolio}

未経験転職モードで特に意識すること：
- 現職の経験を、転職希望職種で評価される言葉に変換する
- 未経験であることを弱みにしすぎず、学習意欲と再現性を伝える
- 現職で培った強みと転職先の業務をつなげる
- 嘘の実績や実務経験は作らない
- 「未経験ですが頑張ります」だけで終わらせない
- なぜその職種を目指すのか、納得感が出るようにする
- 医療職の場合は、正確性・観察力・記録力・改善意識・多職種連携・リスク管理・ユーザー視点を強みに変換する
"""

    job_posting_instruction = ""

    if job_posting:
        job_posting_instruction = f"""
以下は応募予定または参考にする求人票です。

求人票：
{job_posting}

求人票をもとに意識すること：
- 求人票で求められている人物像を読み取る
- 必須スキル・歓迎スキル・業務内容を整理する
- 入力された経験の中から、求人票に合う要素を優先して使う
- 足りないスキルがある場合は、改善アドバイスで正直に伝える
- 求人票の表現を丸写しせず、自然な職務経歴書にする
"""

    prompt = f"""
あなたは転職支援に強いキャリアアドバイザーです。
以下の情報をもとに、職務経歴書の下書きを作成してください。

目的：
転職活動で使える、分かりやすく、誠実で、採用担当者に伝わる職務経歴書を作ること。

文章トーン：
{tone}

入力情報：
現在の職種：{current_job}
経験年数：{experience_years}
転職希望職種：{target_job}

これまでの業務内容：
{work_details}

実績・工夫したこと：
{achievements}

強み：
{strengths}

{career_change_instruction}

{job_posting_instruction}

出力形式：

# 職務経歴書 下書き

## 1. 職務要約
3〜5行で、これまでの経験が伝わる文章を作成してください。

## 2. 職務経歴
業務内容を、採用担当者に伝わりやすい形で整理してください。
箇条書き中心で作成してください。

## 3. 活かせる経験・スキル
転職希望職種に活かせる経験を整理してください。

## 4. 自己PR
強み・実績・仕事への姿勢が伝わる自己PRを作成してください。

## 5. 志望職種との接続ポイント
現職の経験が、転職希望職種でどう活きるのかを整理してください。

## 6. 求人票とのマッチポイント
求人票が入力されている場合は、求人票と本人の経験が合う部分を整理してください。
求人票が入力されていない場合は「求人票が入力されていないため省略」と書いてください。

## 7. 不足しているスキル・今後補うべきこと
正直に、今後学ぶべきことを整理してください。

## 8. 面接で話すべきポイント
面接で深掘りされそうなポイントを3つ出してください。

## 9. そのまま貼り付け用

### 職務要約
職務経歴書にそのまま貼れる文章を作成してください。

### 自己PR
職務経歴書にそのまま貼れる文章を作成してください。

### 志望動機
応募先に合わせて調整しやすい志望動機を作成してください。

## 10. 改善アドバイス
さらに職務経歴書を良くするために、追加で入れるとよい情報を教えてください。

注意点：
- 嘘の実績は作らない
- 数字がない場合は無理に数字を作らない
- 初心者にも分かる自然な表現にする
- 転職書類として失礼のない文章にする
- 未経験転職の場合、学習中の内容や制作物があれば自然にアピールする
- 医療職からIT職への転職では、医療行為そのものではなく、課題発見力・正確性・記録力・改善意識・データ活用への関心を中心に表現する
"""
    return prompt