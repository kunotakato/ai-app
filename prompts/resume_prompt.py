def build_resume_prompt(
    current_job,
    experience_years,
    target_job,
    work_details,
    achievements,
    strengths,
    tone,
):
    """
    職務経歴書を生成するためのプロンプトを作成する関数です。
    入力された情報をもとに、AIへ渡す指示文を作ります。
    """

    prompt = f"""
あなたは転職支援に強いキャリアアドバイザーです。
以下の情報をもとに、職務経歴書の下書きを作成してください。

目的：
転職活動で使える、分かりやすく、誠実で、採用担当者に伝わる職務経歴書を作ること。

文章トーン：
{tone}

入力情報：
現在の職種：{current_job}
経験年数：{experience_years}
転職希望職種：{target_job}

これまでの業務内容：
{work_details}

実績・工夫したこと：
{achievements}

強み：
{strengths}

出力形式：

# 職務経歴書 下書き

## 1. 職務要約
3〜5行で、これまでの経験が伝わる文章を作成してください。

## 2. 職務経歴
業務内容を、採用担当者に伝わりやすい形で整理してください。
箇条書き中心で作成してください。

## 3. 活かせる経験・スキル
転職希望職種に活かせる経験を整理してください。

## 4. 自己PR
強み・実績・仕事への姿勢が伝わる自己PRを作成してください。

## 5. 面接で話すべきポイント
面接で深掘りされそうなポイントを3つ出してください。

## 6. 改善アドバイス
さらに職務経歴書を良くするために、追加で入れるとよい情報を教えてください。

注意点：
- 嘘の実績は作らない
- 数字がない場合は無理に数字を作らない
- 初心者にも分かる自然な表現にする
- 転職書類として失礼のない文章にする
"""
    return prompt