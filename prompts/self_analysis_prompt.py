def build_self_analysis_prompt(
    enjoyable_work,
    difficult_work,
    good_at,
    praised_for,
    avoid_work_style,
    interests,
    learning_skills,
    future_goal,
):
    """
    自己分析AI・職種診断用のプロンプトを作成します。
    """

    prompt = f"""
あなたはキャリア支援に強い自己分析AIです。
以下の入力内容をもとに、ユーザーの強み・向いている職種・転職軸を分析してください。

目的：
ユーザーが自分に合った職種や働き方を理解し、転職活動の方向性を整理できるようにすること。

# 入力情報

楽しかった仕事・作業：
{enjoyable_work}

苦手だった仕事・作業：
{difficult_work}

得意だと思うこと：
{good_at}

人から褒められたこと：
{praised_for}

避けたい働き方：
{avoid_work_style}

興味のある分野：
{interests}

学習中のスキル：
{learning_skills}

将来どうなりたいか：
{future_goal}

# 出力形式

# 自己分析結果

## 1. あなたの強み
入力内容から読み取れる強みを、分かりやすく整理してください。

## 2. 向いている職種TOP5
以下の形式で出してください。

1. 職種名
   - 向いている理由
   - 活かせる強み
   - 注意点

## 3. 向いている理由
なぜその職種が合いそうなのか、共通点を整理してください。

## 4. 避けた方がいい職種・働き方
ユーザーの苦手・避けたい働き方から、合わない可能性がある職種や環境を整理してください。

## 5. 転職軸
転職活動で大事にすべき軸を3〜5個出してください。

## 6. おすすめ求人キーワード
実際に求人サイトで検索しやすいキーワードを10個出してください。

## 7. 今後伸ばすべきスキル
優先順位つきで、今後伸ばすべきスキルを整理してください。

## 8. キャリア戦略アドバイス
3ヶ月・6ヶ月・1年の目安で、現実的な行動プランを提案してください。

注意点：
- 診断結果を断定しすぎない
- ユーザーの可能性を狭めない
- 現実的な選択肢を出す
- 未経験転職の場合は、学習中スキルや制作物も評価する
- 具体的な求人検索キーワードまで出す
- 文章はやさしく、前向きにする
"""
    return prompt