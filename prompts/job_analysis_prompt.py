def build_job_analysis_prompt(
    current_job,
    experience_years,
    target_job,
    work_details,
    achievements,
    strengths,
    learning_skills,
    portfolio,
    job_posting,
):
    """
    求人票とユーザー情報をもとに、求人との相性を分析するプロンプトを作成します。
    """

    prompt = f"""
あなたは転職支援に強いキャリアアドバイザーです。
以下の求人票とユーザー情報をもとに、求人との相性を分析してください。

目的：
ユーザーが応募前に、自分の経験・スキル・制作物が求人にどれくらい合っているかを理解し、
職務経歴書で何を強調すべきか分かるようにすること。

# ユーザー情報

現在の職種：
{current_job}

経験年数：
{experience_years}

転職希望職種：
{target_job}

これまでの業務内容：
{work_details}

実績・工夫したこと：
{achievements}

強み：
{strengths}

学習中のスキル：
{learning_skills}

制作物・ポートフォリオ：
{portfolio}

# 求人票

{job_posting}

# 出力形式

# 求人分析結果

## 1. 求人概要
求人の仕事内容を分かりやすく要約してください。

## 2. 求められるスキル

### 必須スキル
求人票から読み取れる必須スキルを箇条書きで整理してください。

### 歓迎スキル
求人票から読み取れる歓迎スキルを箇条書きで整理してください。

## 3. 求める人物像
求人票から読み取れる人物像を整理してください。

## 4. ユーザー経験とのマッチポイント
ユーザーの経験・強み・学習中スキル・制作物の中から、求人に合っているポイントを整理してください。

## 5. 不足しているスキル
現時点で不足している可能性があるスキルや経験を、正直に整理してください。

## 6. マッチ率
0〜100%で目安を表示してください。

形式：
マッチ率：〇〇%

その理由：
- 理由1
- 理由2
- 理由3

## 7. 職務経歴書で強調すべきポイント
この求人に応募する場合、職務経歴書で強調すべき内容を整理してください。

## 8. 今から補うべき学習内容
応募前・面接前に補うと良い学習内容を、優先順位つきで整理してください。

## 9. 応募前アドバイス
応募するなら何を準備すべきか、現実的にアドバイスしてください。

注意点：
- マッチ率は絶対評価ではなく、あくまで目安として出してください
- 嘘の実績や経験は作らないでください
- 未経験転職の場合は、実務経験と学習経験を分けて評価してください
- 求人票に書かれていない内容を勝手に断定しないでください
- ユーザーの可能性を狭めすぎず、現実的な改善案を出してください
"""
    return prompt