def build_career_map_prompt(
    current_status,
    reason_for_change,
    past_experience,
    worries,
    ideal_work_style,
):
    """
    キャリア地図生成用のプロンプトを作成します。
    """

    prompt = f"""
あなたはキャリア支援に強いAIキャリアコーチです。
以下の情報をもとに、ユーザーのキャリア地図を作成してください。

# 入力情報

現在地：
{current_status}

転職理由・変えたい理由：
{reason_for_change}

過去の経験：
{past_experience}

今の不安：
{worries}

理想の働き方：
{ideal_work_style}

# 出力形式

# キャリア地図

## 1. キャリア現在地
今の状況をやさしく整理してください。

## 2. 強み3つ
過去の経験から読み取れる強みを3つ出してください。

## 3. 転職軸3つ
転職活動で大事にすべき軸を3つ出してください。

## 4. 向いている職種3つ
現実的に向いていそうな職種を3つ出してください。

## 5. 避けた方がいい職場環境
合わない可能性がある環境を整理してください。

## 6. 自己PRに使える経験
職務経歴書や面接で使えそうな経験を整理してください。

## 7. 今日やるべき行動3つ
今日からできる小さな行動を3つ提案してください。

注意点：
- 診断結果を断定しすぎない
- ユーザーの可能性を狭めない
- 現実的で行動しやすい提案にする
- やさしいが、抽象的すぎない文章にする
- 職種提案は理由も添える
"""
    return prompt